-- 10. JOINS EXECUTED CORRECTLY (5 Marks)
-- =====================================================================

-- 10.1 INNER JOIN: Complete booking details with passenger info
SELECT 
    b.id AS booking_id,
    u.username,
    u.first_name || ' ' || u.last_name AS passenger,
    r.departure_location,
    r.destination,
    s.departure_time,
    s.arrival_time,
    b.booking_status,
    b.total_fare
FROM core_booking b
JOIN core_user u ON b.user_id = u.id
JOIN core_schedule s ON b.schedule_id = s.id
JOIN core_route r ON s.route_id = r.id
ORDER BY b.booking_date DESC;

-- 10.2 LEFT JOIN: All users with their bookings (including users with no bookings)
SELECT 
    u.id,
    u.username,
    u.email,
    COUNT(b.id) AS total_bookings
FROM core_user u
LEFT JOIN core_booking b ON u.id = b.user_id
GROUP BY u.id, u.username, u.email
ORDER BY total_bookings DESC;

-- 10.3 Multi-table JOIN: Booking + Payment + Ticket
SELECT 
    b.id AS booking_id,
    u.username,
    t.seat_number,
    t.ticket_status,
    p.amount AS payment_amount,
    p.payment_method,
    p.payment_status
FROM core_booking b
JOIN core_user u ON b.user_id = u.id
JOIN core_ticket t ON b.id = t.booking_id
LEFT JOIN core_payment p ON b.id = p.booking_id
ORDER BY b.id;

-- 10.4 JOIN with schedule, route, and bus
SELECT 
    s.id AS schedule_id,
    r.departure_location || ' → ' || r.destination AS route,
    b.registration_number AS bus,
    b.bus_type,
    s.departure_time,
    s.travel_date,
    b.capacity,
    COUNT(t.id) AS booked_seats
FROM core_schedule s
JOIN core_route r ON s.route_id = r.id
JOIN core_bus b ON s.bus_id = b.id
LEFT JOIN core_booking bk ON s.id = bk.schedule_id AND bk.booking_status = 'BOOKED'
LEFT JOIN core_ticket t ON bk.id = t.booking_id AND t.ticket_status = 'ACTIVE'
GROUP BY s.id, r.departure_location, r.destination, b.registration_number, 
         b.bus_type, s.departure_time, s.travel_date, b.capacity
ORDER BY s.departure_time;

-- 10.5 Self-referencing JOIN: Users assigned roles by other users
SELECT 
    u1.username AS assigned_user,
    r.role_name,
    u2.username AS assigned_by_admin,
    ur.assigned_date
FROM core_userrole ur
JOIN core_user u1 ON ur.user_id = u1.id
JOIN core_role r ON ur.role_id = r.id
LEFT JOIN core_user u2 ON ur.assigned_by_id = u2.id
ORDER BY ur.assigned_date DESC;


-- =====================================================================
-- 11. SUB-QUERIES USED EFFECTIVELY (5 Marks)
-- =====================================================================

-- 11.1 Subquery in WHERE: Users who have made at least one booking
SELECT id, username, email, first_name, last_name
FROM core_user
WHERE id IN (SELECT DISTINCT user_id FROM core_booking);

-- 11.2 Subquery with NOT IN: Users who have NEVER made a booking
SELECT id, username, email
FROM core_user
WHERE id NOT IN (SELECT DISTINCT user_id FROM core_booking);

-- 11.3 Subquery with EXISTS: Routes that have schedules
SELECT id, departure_location, destination
FROM core_route r
WHERE EXISTS (SELECT 1 FROM core_schedule s WHERE s.route_id = r.id);

-- 11.4 Subquery in SELECT: Average fare compared to overall average
SELECT 
    b.id,
    b.total_fare,
    (SELECT ROUND(AVG(total_fare), 2) FROM core_booking) AS overall_avg_fare,
    ROUND(b.total_fare - (SELECT AVG(total_fare) FROM core_booking), 2) AS difference_from_avg
FROM core_booking b
LIMIT 10;

-- 11.5 Correlated subquery: Users who spent more than average
SELECT 
    u.username,
    SUM(b.total_fare) AS total_spent
FROM core_user u
JOIN core_booking b ON u.id = b.user_id
GROUP BY u.id, u.username
HAVING SUM(b.total_fare) > (SELECT AVG(total_fare) FROM core_booking);

-- 11.6 Subquery in FROM: Derived table for analytics
SELECT 
    route_stats.departure_location,
    route_stats.destination,
    route_stats.booking_count,
    route_stats.avg_fare
FROM (
    SELECT 
        r.departure_location,
        r.destination,
        COUNT(b.id) AS booking_count,
        ROUND(AVG(b.total_fare), 2) AS avg_fare
    FROM core_route r
    JOIN core_schedule s ON r.id = s.route_id
    JOIN core_booking b ON s.id = b.schedule_id
    WHERE b.booking_status = 'BOOKED'
    GROUP BY r.id, r.departure_location, r.destination
) AS route_stats
WHERE route_stats.booking_count > 1
ORDER BY route_stats.booking_count DESC;