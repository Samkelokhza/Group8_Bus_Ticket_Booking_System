-- 6.1 ROUND payment amounts to 2 decimal places
SELECT 
    id,
    amount,
    ROUND(amount, 2) AS rounded_amount,
    TRUNC(amount) AS truncated_amount
FROM core_payment;

-- 6.2 ROUND average fare per route
SELECT 
    r.departure_location,
    r.destination,
    ROUND(AVG(b.total_fare), 2) AS avg_fare_rounded,
    TRUNC(AVG(b.total_fare)) AS avg_fare_truncated
FROM core_route r
JOIN core_schedule s ON r.id = s.route_id
JOIN core_booking b ON s.id = b.schedule_id
GROUP BY r.id, r.departure_location, r.destination;

-- 6.3 CEIL and FLOOR functions
