-- 1.1 Find all available schedules for a specific route
SELECT s.id, r.departure_location, r.destination, s.departure_time, s.arrival_time, 
       b.bus_type, b.registration_number, s.travel_date
FROM core_schedule s
JOIN core_route r ON s.route_id = r.id
JOIN core_bus b ON s.bus_id = b.id
WHERE r.departure_location = 'Johannesburg' 
  AND r.destination = 'Durban'
  AND s.departure_time > NOW()
ORDER BY s.departure_time;

-- 1.2 View all bookings for a specific passenger
SELECT b.id AS booking_id, r.departure_location, r.destination, 
       s.departure_time, b.booking_status, b.total_fare, b.booking_date
FROM core_booking b
JOIN core_schedule s ON b.schedule_id = s.id
JOIN core_route r ON s.route_id = r.id
WHERE b.user_id = 1
ORDER BY b.booking_date DESC;

-- 1.3 Total revenue generated by the system
SELECT COALESCE(SUM(amount), 0) AS total_revenue,
       COUNT(*) AS total_payments
FROM core_payment
WHERE payment_status = 'PAID';

-- 1.4 List all unresolved complaints
SELECT c.id, u.username, u.email, c.description, c.date_submitted, c.resolution_status
FROM core_complaint c
JOIN core_user u ON c.user_id = u.id
WHERE c.resolution_status != 'resolved'
ORDER BY c.date_submitted ASC;

-- 1.5 View all schedules for a specific date
SELECT s.id, r.departure_location, r.destination, s.departure_time, 
       b.registration_number, b.capacity
FROM core_schedule s
JOIN core_route r ON s.route_id = r.id
JOIN core_bus b ON s.bus_id = b.id
WHERE s.travel_date = CURRENT_DATE + INTERVAL '1 day'
ORDER BY s.departure_time;

-- 1.6 Count bookings per route (popularity report)
SELECT r.departure_location, r.destination, COUNT(b.id) AS total_bookings
FROM core_route r
LEFT JOIN core_schedule s ON r.id = s.route_id
LEFT JOIN core_booking b ON s.id = b.schedule_id AND b.booking_status = 'BOOKED'
GROUP BY r.id, r.departure_location, r.destination
ORDER BY total_bookings DESC;

-- 1.7 Find passengers with their next of kin details
SELECT u.username, u.first_name, u.last_name, u.phone_number,
       nk.kin_fullname, nk.contact_number
FROM core_user u
LEFT JOIN core_nextofkin nk ON u.id = nk.user_id
ORDER BY u.last_name;

-- 1.8 View all active buses
SELECT id, bus_type, capacity, registration_number
FROM core_bus
WHERE id IN (SELECT DISTINCT bus_id FROM core_schedule WHERE departure_time > NOW());

-- 1.9 Payment summary by method
SELECT payment_method, COUNT(*) AS count, SUM(amount) AS total
FROM core_payment
WHERE payment_status = 'PAID'
GROUP BY payment_method;

-- 1.10 Users who have made bookings but no payments
SELECT DISTINCT u.username, u.email
FROM core_user u
JOIN core_booking b ON u.id = b.user_id
WHERE b.booking_status = 'BOOKED'
  AND b.id NOT IN (SELECT booking_id FROM core_payment WHERE payment_status = 'PAID');


