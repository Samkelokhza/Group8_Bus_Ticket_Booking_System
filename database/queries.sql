-- 8.1 COUNT, SUM, AVG, MAX, MIN on payments
SELECT 
    COUNT(*) AS total_payments,
    SUM(amount) AS total_revenue,
    AVG(amount) AS average_payment,
    MAX(amount) AS largest_payment,
    MIN(amount) AS smallest_payment
FROM core_payment
WHERE payment_status = 'PAID';

-- 8.2 COUNT bookings per user
SELECT 
    u.username,
    COUNT(b.id) AS booking_count,
    COALESCE(SUM(b.total_fare), 0) AS total_spent,
    COALESCE(AVG(b.total_fare), 0) AS avg_fare
FROM core_user u
LEFT JOIN core_booking b ON u.id = b.user_id AND b.booking_status = 'BOOKED'
GROUP BY u.id, u.username
ORDER BY booking_count DESC;

-- 8.3 COUNT tickets per schedule
SELECT 
    s.id AS schedule_id,
    r.departure_location,
    r.destination,
    COUNT(t.id) AS tickets_issued,
    b.capacity AS total_capacity
FROM core_schedule s
JOIN core_route r ON s.route_id = r.id
JOIN core_bus b ON s.bus_id = b.id
LEFT JOIN core_booking bk ON s.id = bk.schedule_id
LEFT JOIN core_ticket t ON bk.id = t.booking_id AND t.ticket_status = 'ACTIVE'
GROUP BY s.id, r.departure_location, r.destination, b.capacity
ORDER BY tickets_issued DESC;


-- =====================================================================
-- 9. GROUP BY & HAVING CLAUSES (4 Marks)
-- =====================================================================

-- 9.1 Routes with more than 1 booking
SELECT 
    r.departure_location,
    r.destination,
    COUNT(b.id) AS booking_count
FROM core_route r
LEFT JOIN core_schedule s ON r.id = s.route_id
LEFT JOIN core_booking b ON s.id = b.schedule_id
GROUP BY r.id, r.departure_location, r.destination
HAVING COUNT(b.id) > 1
ORDER BY booking_count DESC;

-- 9.2 Users who have spent more than R300
SELECT 
    u.username,
    u.first_name || ' ' || u.last_name AS full_name,
    SUM(b.total_fare) AS total_spent
FROM core_user u
JOIN core_booking b ON u.id = b.user_id
WHERE b.booking_status = 'BOOKED'
GROUP BY u.id, u.username, u.first_name, u.last_name
HAVING SUM(b.total_fare) > 300
ORDER BY total_spent DESC;

-- 9.3 Payment methods used more than once
SELECT 
    payment_method,
    COUNT(*) AS times_used,
    SUM(amount) AS total_collected
FROM core_payment
WHERE payment_status = 'PAID'
GROUP BY payment_method
HAVING COUNT(*) > 1
ORDER BY times_used DESC;

-- 9.4 Schedules with more than 2 tickets booked
SELECT 
    s.id AS schedule_id,
    r.departure_location || ' → ' || r.destination AS route,
    COUNT(t.id) AS tickets_booked
FROM core_schedule s
JOIN core_route r ON s.route_id = r.id
JOIN core_booking b ON s.id = b.schedule_id
JOIN core_ticket t ON b.id = t.booking_id AND t.ticket_status = 'ACTIVE'
GROUP BY s.id, r.departure_location, r.destination
HAVING COUNT(t.id) > 2
ORDER BY tickets_booked DESC;


