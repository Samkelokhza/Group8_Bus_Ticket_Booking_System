-- 7.3 Days until departure for upcoming schedules
SELECT 
    id,
    departure_time,
    travel_date,
    EXTRACT(DAY FROM (departure_time - NOW())) AS days_until_departure,
    EXTRACT(HOUR FROM (departure_time - NOW())) AS hours_until_departure
FROM core_schedule
WHERE departure_time > NOW()
ORDER BY departure_time;

-- 7.4 Date formatting with TO_CHAR
SELECT 
    id,
