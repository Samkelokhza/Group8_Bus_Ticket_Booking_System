LIMIT 5;

-- 7.6 AGE function (time between two dates)
SELECT 
    c.id,
    c.date_submitted,
    COALESCE(c.date_resolved, NOW()) AS resolved_or_now,
    AGE(COALESCE(c.date_resolved, NOW()), c.date_submitted) AS complaint_age
FROM core_complaint c;


